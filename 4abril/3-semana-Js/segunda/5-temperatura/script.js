/*5) Programar a conversão de uma temperatura 
digitada pelo usuário em graus Celsius para Fahrenheit.
 Mostrar o resultado na tela.*/


 let temperaturaF,temperaturaC,tempconverter

 temperaturaF=Number(prompt("Qual a temperatura voce quer converter:"))
temperaturaC=5/9

 tempconverter=(temperaturaF-32)*temperaturaC

 alert(tempconverter)